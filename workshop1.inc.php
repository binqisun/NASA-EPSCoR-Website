	<p class="leading">
The robotics workshop was a huge hit with the kids. The kids were split up into 3 groups where one group learned how to operate Six, while another group learned to control JD, and finally the younger kids worked on the EV3. Our three instructors were undergraduate students Joe Hilton who taught Six, Reese Childers who taught JD, and Oliver Roundtree who taught the Lego Mindstorms EV3 robot. The morning started off with us showing the groups the basics of robotics and programming. We used hands on examples to help illustrate the more complex concepts of programming robots, such as using sensors to interact with the environment. After a quick snack, we assigned the kids to complete a lab with the instructor&#39;s assistance. The groups were able to complete the labs surprisingly fast and had time to improve their projects. Overall we had a lot of fun and can&#39;t wait for the next workshop!	
	</p>
