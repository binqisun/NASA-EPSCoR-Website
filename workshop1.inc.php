	<h1 style="text-align:center; font-size: 2.2em;">Robotics Workshop, Apr. 4, 2015</h1>
	<br>
	<p class="leading">
On Saturday Apr. 4, sponsored by NASA EPSCoR grant, Dr. Sun, her three research students (Reese Childers, Oliver Roundtree, Joseph Hiliton) from Computer Science and STEM Institute jointly hosted the second free robotics camp to the 5th - 8th graders who are from different schools such as LISA Academy. In this camp, we introduced the basics of robotics and taught the students how to construct, program, and control robots. Then, students worked on their projects and made their own programs to control robots successfully including dancing, moving, color detection, and voice recognition. Many students told us this was their first time to learn programming and work with robotics. They learned a lot and had a blast! We also received lots of appreciation from parents and students.
	</p>
