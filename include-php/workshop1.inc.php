	<h1 style="text-align:center; font-size: 2.2em;">Robotics Workshop, Feb. 28, 2015</h1>
	<br>
	
	<p class="leading">
Sponsored by NASA EPSCoR grant, Dr.Sun, her three students (Reese Childers, Oliver Roundtree, Joseph Hiliton) from Computer Science and STEM Institute jointly offered a free robotics camp to 5th – 8th grade students on Saturday Feb. 28. Within 24 hours, the spots were fully filled and there were still 11 students on the waiting list. Twenty-five students from different public/private schools attended this camp (even two parents stayed in the camp to learn the robotics). In this camp, we introduced the basics of robotics, and taught students how to construct, program, and control robots. After that, students worked on their projects and made their own programs to control robots successfully including dancing, moving, color detection, and voice recognition.  Many students told us this was their first time to learn programming and work with robotics. They really enjoyed the camp and learned a lot. They even asked when will be the next robotics camp before they left  
	</p>

