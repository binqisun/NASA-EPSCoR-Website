	<h1 style="text-align:center; font-size: 2.2em;">Robotics Workshop, Nov. 19, 2016</h1>
	<br>
	<p class="leading">
			
	On Saturday Nov. 19, sponsored by NASA EPSCoR grant, Dr. Sun, her Eight volunteers and research students (Sravani Marreddy, Reese Childers, Joseph Hilton, Min Ji Kim, Yusei Harada, Deana Johnson, Desire Johnson, Ellie Feng) from Computer Science and STEM Institute jointly hosted the third free robotics camp to the 5th - 8th graders who are from different schools such as Ruth Doyle Middle School. In this camp, we introduced the basics of robotics, and taught students how to construct, program, and control robots. After that, students worked on their projects and made their own programs to control robots successfully including dancing, moving, color detection, and voice recognition. 
	</p>
