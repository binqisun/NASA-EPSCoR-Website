	<p>
	The Funded research, entitled "New Computer Vision Methods for NASA Robotic Planetary Exploration", is a multi-disciplinary collabrotive project among UALR, UCA, and UA-Fayetteville. The total Amount of the award is $750,000 for the duration of 36 months. The goal of NASA EPSCoR is to enable jurisdictions to develop an academic research enterprise directed toward long-term, self-sustaining, nationally-competitive capabilities in aerospace-related research.
	</p>
	
	</div>
	<br>
	<br>
	<p>
	In this project, the multi-campus research teams of Dr. Ye Cang (PI, UALR), Dr. Yu Sun (Co-PI, UCA), and Dr. Miaoqing Huang (Co-PI, UA) will collaboratively devise innovative computer vision methods based on a new class of 3D imaging and camera sensor to support autonomous robotic operation in NASA's future missions. The proposed methods are expected to revolutionize robotic autonomy and advance NASA's robotic technology. In addition to the great economic benefit to the state of Arkansas, this project will also have significant educational impact in providing highly trained undergraduates and graduates in the state to support the growth of the knowledge-based industry.
	</p>
